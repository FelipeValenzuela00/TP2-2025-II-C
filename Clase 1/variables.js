// Variables EN javascript



// var es un tipo de variable, que se puede redeclerar, y reasignar
// var no es recomendado en codigo moderno
var nombre = "Matias"
console.log("LOG variableNombre: ", nombre);

nombre = "Alan"
console.log("LOG variableNombre: ", nombre);

var nombre = "Juan"
console.log("LOG variableNombre: ", nombre);

// let, se puede reasignar, pero no redeclarar, en el mismo scope

let edad = 30

console.log("Edad con Let: ", edad)

edad = 20
console.log("Edad con Let: ", edad)


// const, no se puede reasignar, ni redeclarar

const apellido = 'Duro'

console.log('Apellido: ', apellido);


